package com.example.androidzonghe1.entity.yjWork;

public class Parent {
    private int id;













































    private String name;
    private String child_name;//孩子名字
    private String phone;//电话号码
    private String relat;//关系
    private String addr;//学校地址
    private String destinct;//目的地
    public int getId() {
        return id;
    }

    public Parent() {
    }

    public void setId(int id) {
        this.id = id;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getChild_name() {
        return child_name;
    }
    public void setChild_name(String child_name) {
        this.child_name = child_name;
    }
    public String getPhone() {
        return phone;
    }
    public void setPhone(String phone) {
        this.phone = phone;
    }
    public String getRelat() {
        return relat;
    }
    public void setRelat(String relat) {
        this.relat = relat;
    }
    public String getAddr() {
        return addr;
    }
    public void setAddress(String address) {
        this.addr = address;
    }
    public String getDestinction() {
        return destinct;
    }
    public void setDestinction(String destinction) {
        this.destinct = destinction;
    }

}
